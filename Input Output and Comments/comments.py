# Input example
user_name = input("Enter your name: ")

# Output example
print("Hello, " + user_name + "! Welcome to Python.")

# Formatted Output example using f-strings
age = input("Enter your age: ")
print(f"Hello, {user_name}! You are {age} years old.")  # Output with user's name and age

# Another way of formatted output using format() method
print("Hello, {}! You are {} years old.".format(user_name, age))  # Output using format() method

