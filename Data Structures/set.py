# Creating a set
fruits_set = {"apple", "banana", "cherry"}

# Adding elements
fruits_set.add("orange") # Adds orange to the set
print(fruits_set)  # Output: {"apple", "banana", "cherry", "orange"}

# Removing elements
fruits_set.remove("banana") # Removes banana from the set
print(fruits_set)  # Output: {"apple", "cherry", "orange"}
